import math

#Funcion:
def raizcua(valor):
    raiz = math.sqrt(valor)
    return raiz


numero1 = 4
numero2 = 9
numero3 = 16


raiz1 = raizcua(numero1)
raiz2 = raizcua(numero2)
raiz3 = raizcua(numero3)

print("La raiz de ", numero1, " es: ", raiz1)
print("La raiz de ", numero2, " es: ", raiz2)
print("La raiz de ", numero3, " es: ", raiz3)
